public class PrimitiveDatatypes {
    public static void main(String[] args) {
        byte by = 1;
        short sh = 197;
        int it = 2009;
        long ln = 200945678L;

        float ft = 3.14F;
        double db = 3.141597845;

        char ch1 = 'a';
        char ch2 = '1'; // As 1 is inside single inverted comma, hence it will be treated as character.

        boolean tru = true;
        boolean fal = false;
    }
}
