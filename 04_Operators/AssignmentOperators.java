public class AssignmentOperators {
    public static void main(String[] args) {
        int number = 7;

        number += 6;
        System.out.println(number);

        number -= 6;
        System.out.println(number);

        number *= 6;
        System.out.println(number);

        number /= 6;
        System.out.println(number);
    }
}
