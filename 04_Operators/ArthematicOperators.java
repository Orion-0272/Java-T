public class ArthematicOperators {
    public static void main(String[] args) {
        int num1 = 4;
        int num2 = 5;

        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num2 / num1);
        System.out.println(num1 % num2);
    }
}
